/**
 * Counts occurences of characters
 * @returns 
 */
export function chars() {
    var res = {};
    for (let i = 0; i < attributes.length; i++) {
        var elem = attributes[i];
        if (elem.name) {
            for (var j = 0; j < elem.name.length; j++) {
                var c = elem.name[j];
                res[c] = (res[c] || 0) + 1;
            }
        }
    }
    return res;
}

